CREATE OR REPLACE FORCE VIEW GD4_TREE_USER_JS
(PARENTID, NODEID, TEXT, URL, TOOLTIP, 
 CUSTOM_IMAGE, DEFAULT_IMAGE, CUSTOM_STYLE, ID_CARTELLA, TIPO_OGGETTO, 
 ID_OGGETTO)
